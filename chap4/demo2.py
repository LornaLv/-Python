# 测试对象的布尔值
print('-----------以下对象的布尔值为False，除此之外，其他对象的布尔值均为Ture------------')
print(bool(False))  # False
print(bool(0))
print(bool(0.0))
print(bool(None))
print(bool(''))  # 空字符串
print(bool(""))  # 空字符串
print(bool([]))  # 空列表
print(bool(list()))  # 空列表
print(bool(()))  # 空元组
print(bool(tuple()))  # 空元组
print(bool({}))  # 空字典
print(bool(dict()))  # 空字典
print(bool(set()))  # 空集合
