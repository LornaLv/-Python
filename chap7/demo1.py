# 字典的创建方式
print('---------------使用{}创建字典----------------')
scores = {'张三': 100, '李四': 98, '王五': 67}
print(scores)
print(type(scores))

print('---------------使用内置函数dict 创建字典----------------')
student = dict(name='jack', age=19)
print(student)

print('---------------空字典----------------')
d = {}
print(d)
